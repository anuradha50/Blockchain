console.log("Comparison & Logical operators");

/*
Comparison
1. > Greater than
2. < Less than
3. >= Greater than or equal to
4. <= Less than or equal to
5. == Equals (value)
6. != Not equals
7. === Strict equality (Type + value)
8. !== Not strictly equal(Type + value)

Logical
1. AND -> &&
2. OR -> ||
*/

